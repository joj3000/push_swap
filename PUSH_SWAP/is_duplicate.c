#include "push_swap.h"

int	is_duplicate(int ac, char **av)
{
	int i;
	int j;

	i = 1;
	j = 2;
	while (i < ac)
	{
		while (j < ac)
		{
			if (ft_strcmp(av[i], av[j]) == 0)
				return (0);
			j++;
		}
		i++;
		j = i + 1;
	}
	return (1);
}
