#include "push_swap.h"

int	strln_s(char *s)
{
	int i;
	int neg;

	i = 0;
	neg = 0;
	if (s[i] == '-')
	{
		i++;
		neg = 1;
	}
	while (s[i])
		i++;
	return (i - neg);
}
